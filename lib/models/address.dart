class Address {
  final int id;
  final String street;
  final int number;
  final String complement;
  final String city;
  final String state;
  final String zipCode;

  Address({
    required this.id,
    required this.street,
    required this.number,
    required this.complement,
    required this.city,
    required this.state,
    required this.zipCode,
  });
}
